package com.hkha.ea.dto.assess;

public class SignatureInfo {

	private String nameOfAppraisee;
	
	private String appraiseeRankPost;
	
	private String appraiseeSignDate;
	
	private String officerName;
	
	private String officerRank;
	
	private String officerDate;
	
	private String tel;

	public String getNameOfAppraisee() {
		return nameOfAppraisee;
	}

	public void setNameOfAppraisee(String nameOfAppraisee) {
		this.nameOfAppraisee = nameOfAppraisee;
	}

	public String getAppraiseeRankPost() {
		return appraiseeRankPost;
	}

	public void setAppraiseeRankPost(String appraiseeRankPost) {
		this.appraiseeRankPost = appraiseeRankPost;
	}

	public String getAppraiseeSignDate() {
		return appraiseeSignDate;
	}

	public void setAppraiseeSignDate(String appraiseeSignDate) {
		this.appraiseeSignDate = appraiseeSignDate;
	}

	public String getOfficerName() {
		return officerName;
	}

	public void setOfficerName(String officerName) {
		this.officerName = officerName;
	}

	public String getOfficerRank() {
		return officerRank;
	}

	public void setOfficerRank(String officerRank) {
		this.officerRank = officerRank;
	}

	public String getOfficerDate() {
		return officerDate;
	}

	public void setOfficerDate(String officerDate) {
		this.officerDate = officerDate;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}



	
}
