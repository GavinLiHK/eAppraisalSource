package com.hkha.ea.dto.assess;

public class TrainPlanInfo {
	
	private Long reportId;
	
	private Long seqNo;
	
	private String delevelopNeed;
	
	private String actionPlan;
	
	private String reviewDate;

	public Long getReportId() {
		return reportId;
	}

	public void setReportId(Long reportId) {
		this.reportId = reportId;
	}

	public Long getSeqNo() {
		return seqNo;
	}

	public void setSeqNo(Long seqNo) {
		this.seqNo = seqNo;
	}

	public String getDelevelopNeed() {
		return delevelopNeed;
	}

	public void setDelevelopNeed(String delevelopNeed) {
		this.delevelopNeed = delevelopNeed;
	}

	public String getActionPlan() {
		return actionPlan;
	}

	public void setActionPlan(String actionPlan) {
		this.actionPlan = actionPlan;
	}

	public String getReviewDate() {
		return reviewDate;
	}

	public void setReviewDate(String reviewDate) {
		this.reviewDate = reviewDate;
	}
	
	

}
