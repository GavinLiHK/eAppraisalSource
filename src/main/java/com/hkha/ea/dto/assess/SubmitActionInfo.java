package com.hkha.ea.dto.assess;

public class SubmitActionInfo {
	
	private String submitAction ;
	private String submitToEmployeeNum ;
	private String submitNextDeadDate ;
	private String submitNotificationContent ;
	private String submitToAddress ;
	private String newStatus ;
	public String getSubmitAction() {
		return submitAction;
	}
	public void setSubmitAction(String submitAction) {
		this.submitAction = submitAction;
	}
	public String getSubmitToEmployeeNum() {
		return submitToEmployeeNum;
	}
	public void setSubmitToEmployeeNum(String submitToEmployeeNum) {
		this.submitToEmployeeNum = submitToEmployeeNum;
	}
	public String getSubmitNextDeadDate() {
		return submitNextDeadDate;
	}
	public void setSubmitNextDeadDate(String submitNextDeadDate) {
		this.submitNextDeadDate = submitNextDeadDate;
	}
	public String getSubmitNotificationContent() {
		return submitNotificationContent;
	}
	public void setSubmitNotificationContent(String submitNotificationContent) {
		this.submitNotificationContent = submitNotificationContent;
	}
	public String getSubmitToAddress() {
		return submitToAddress;
	}
	public void setSubmitToAddress(String submitToAddress) {
		this.submitToAddress = submitToAddress;
	}
	public String getNewStatus() {
		return newStatus;
	}
	public void setNewStatus(String newStatus) {
		this.newStatus = newStatus;
	}
	
	
}
